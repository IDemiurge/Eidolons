package main.system.test;

import java.util.List;

public class TestingConfigurations {
    Boolean auto;

    List<String> testList;
    List<String> autoActionList;

    public Boolean getAuto() {
        return auto;
    }

    public void setAuto(Boolean auto) {
        this.auto = auto;
    }

    public List<String> getTestList() {
        return testList;
    }

    public void setTestList(List<String> testList) {
        this.testList = testList;
    }

    public List<String> getAutoActionList() {
        return autoActionList;
    }

    public void setAutoActionList(List<String> autoActionList) {
        this.autoActionList = autoActionList;
    }

}
