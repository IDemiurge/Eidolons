package eidolons.libgdx.particles.spell;

import com.badlogic.gdx.graphics.g2d.Batch;
import eidolons.libgdx.particles.EmitterActor;
import eidolons.libgdx.particles.ParticleEffectX;

/**
 * Created by JustMe on 11/13/2018.
 */
public class SpellVfx extends EmitterActor {

    public SpellVfx(String path) {
        super(path);
    }

    @Override
    public void act(float delta) {
        super.act(delta);
    }

    @Override
    public void updatePosition(float x, float y) {
        super.updatePosition(x, y);
    }

    @Override
    public void draw(Batch batch, float parentAlpha) {
            super.draw(batch, parentAlpha);
    }

    @Override
    public ParticleEffectX getEffect() {
        return super.getEffect();
    }

    @Override
    public void start() {
        super.start();
    }

    @Override
    public String getPath() {
        return super.getPath();
    }
}
